export function SearchDialog({ setOpen, open }: { setOpen: (b: boolean) => void, open: boolean }) {
    const { assets } = useAppContext()
    const [query, setQuery] = useState<string>('')
    const [plainAssets, setPlainAssets] = useState<any[]>([])

    useEffect(() => {
        console.log({ assets })
        // setPlainAssets(Object.values(assets).map((resource: any) => resource.assets?.assets.map((asset: any) => ({ ...asset, value: (asset as any).name }))).flat())
    }, [assets])


    const router = useRouter()

    return (
        <>
            <CommandDialog open={open} onOpenChange={setOpen}>
                <Command filter={(value, search) => {
                    return value.toLowerCase().includes(search.toLowerCase()) ? 1 : 0
                }}>
                    <CommandInput onChangeCapture={(e) => setQuery((e.target as any).value)} placeholder="Search..." />
                    <CommandList>
                        <CommandGroup heading={`Assets`}>
                            {plainAssets.map((asset: any, i: number) => (
                                <CommandItem onSelect={() => {
                                    router.push(`/lineage/${asset.id}`)
                                    setOpen(false)
                                }
                                } key={i}>
                                    <div className='space-x-2 flex'>
                                        <div>
                                            {getAssetIcon(asset.type, asset.config?.dialect || asset.resource_type)}
                                        </div>
                                        <div>
                                            {asset.name}
                                        </div>
                                    </div>
                                </CommandItem>
                            ))}
                        </CommandGroup>
                        <CommandEmpty>
                            No results found.
                        </CommandEmpty>
                    </CommandList>
                </Command>
            </CommandDialog >
        </>
    )
}
